                || ID_PACE_DH_IM_AES_CBC_CMAC_192 == oid
                || ID_PACE_ECDH_IM_AES_CBC_CMAC_192 == oid
                || ID_PACE_ECDH_CAM_AES_CBC_CMAC_192 == oid
            ) {
                return 192
            } else if (ID_PACE_DH_GM_AES_CBC_CMAC_256 == oid
                || ID_PACE_DH_IM_AES_CBC_CMAC_256 == oid
                || ID_PACE_ECDH_GM_AES_CBC_CMAC_256 == oid
                || ID_PACE_ECDH_IM_AES_CBC_CMAC_256 == oid
                || ID_PACE_ECDH_CAM_AES_CBC_CMAC_256 == oid
            ) {
                return 256
            }

            throw NumberFormatException("Unknown OID: \"$oid\"")
        }

        /**
         * Derives a JCE algorithm parameter specification from a PACE standard domain parameter integer.
         * 
         * @param stdDomainParam the standard domain parameter
         * 
         * @return a JCE algorithm parameter specification
         */
        fun toParameterSpec(stdDomainParam: BigInteger): AlgorithmParameterSpec? {
            return toParameterSpec(stdDomainParam.toInt())
        }

