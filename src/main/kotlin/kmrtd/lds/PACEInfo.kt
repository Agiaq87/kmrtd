/**
 * PACE Info object as per SAC TR 1.01, November 11, 2010.
 * 
 * @author The JMRTD team (info@jmrtd.org)
 * 
 * @version $Revision: 1850 $
 * 
 * @since 0.5.0
 */
class PACEInfo(oid: String?, version: Int, parameterId: BigInteger?) : SecurityInfo() {
    /**
     * A DH parameter specification which also keeps track of
     * the prime order of the subgroup generated by the generator.
     * 
     * @author The JMRTD team (info@jmrtd.org)
     * 
     * @version $Revision: 1850 $
     */
    class DHCParameterSpec
    /**
     * Creates a parameter specification.
     * 
     * @param p the prime
     * @param g the generator
     * @param q the prime order of subgroup generated by `g`
     */(
        p: BigInteger?, g: BigInteger?,
        /**
         * Returns the prime order of subgroup generated by `g`.
         * 
         * @return the prime order of subgroup generated by `g`
         */
        val q: BigInteger?
    ) : DHParameterSpec(p, g)

    /** Generic mapping and Integrated mapping and CAM mapping.  */
    enum class MappingType {
        /** Generic Mapping.  */
        GM,

        /** Integrated Mapping.  */
        IM,

        /** Chip Authentication Mapping.  */
        CAM
    }

    /**
     * Returns the PACE protocol object identifier.
     * 
     * @return the PACE protocol object identifier
     */
    override val objectIdentifier: String?

    /**
     * Returns the version.
     * 
     * @return the version
     */
    val version: Int

    /**
     * Returns the parameter identifier.
     * 
     * @return the parameter identifier
     */
    val parameterId: BigInteger?

    /**
     * Constructs a PACE info object.
     * 
     * @param oid the object identifier, indicating what PACE variant
     * is to be used (agreement protocol, mapping type, and secure channel properties)
     * @param version a version number, which should be 2
     * @param parameterId either a standardized domain parameter id from table 6 or a proprietary domain parameter
     */
    constructor(oid: String?, version: Int, parameterId: Int) : this(
        oid,
        version,
        BigInteger.valueOf(parameterId.toLong())
    )

    /**
     * Creates a PACE info object.
     * 
     * @param oid the object identifier, indicating what PACE variant
     * is to be used (agreement protocol, mapping type, and secure channel properties)
     * @param version a version number, which should be 2
     * @param parameterId either a standardized domain parameter id from table 6 or a proprietary domain parameter
     */
    init {
        require(checkRequiredIdentifier(oid)) { "Invalid OID" }
        if (version != VERSION_2) {
            LOGGER.warning("Invalid version, must be 2")
        }
        this.objectIdentifier = oid
        this.version = version
        this.parameterId = parameterId
    }

    override val protocolOIDString: String?
        /**
         * Returns the protocol object identifier as a human readable string.
         * 
         * @return a string describing the PACE protocol object identifier
         */
        get() = toProtocolOIDString(this.objectIdentifier)

    @get:Deprecated("this method will be removed from visible interface (because of dependency on BC API)")
    override val dERObject: ASN1Primitive
        /**
         * Returns a DER object with this SecurityInfo data (DER sequence).
         * 
         * @return a DER object with this SecurityInfo data
         * 
         */
        get() {
            val vector = ASN1EncodableVector()

            /* Protocol */
            vector.add(ASN1ObjectIdentifier(this.objectIdentifier))

            /* Required data */
            vector.add(ASN1Integer(version.toLong()))

            /* Optional data */
            if (parameterId != null) {
                vector.add(ASN1Integer(parameterId))
            }
            return DLSequence(vector)
        }

    override fun toString(): String {
        val result = StringBuilder()
        result.append("PACEInfo [")
        result.append("protocol: ").append(toProtocolOIDString(this.objectIdentifier))
        result.append(", version: ").append(version)
        if (parameterId != null) {
            result.append(", parameterId: ").append(toStandardizedParamIdString(parameterId))
        }
        result.append("]")
        return result.toString()
    }

    override fun hashCode(): Int {
        return (1234567891
                + 7 * objectIdentifier.hashCode() + 5 * version + 3 * (parameterId?.hashCode() ?: 1991))
    }

    override fun equals(other: Any?): Boolean {
        if (other == null) {
            return false
        }
        if (other === this) {
            return true
        }
        if (PACEInfo::class.java != other.javaClass) {
            return false
        }

        val otherPACEInfo = other as PACEInfo

        if (this.objectIdentifier == null && otherPACEInfo.objectIdentifier != null) {
            return false
        }
        if (this.objectIdentifier != null && this.objectIdentifier != otherPACEInfo.objectIdentifier) {
            return false
        }

        if (version != otherPACEInfo.version) {
            return false
        }

        if (parameterId == null && otherPACEInfo.parameterId != null) {
            return false
        }
        if (parameterId != null && parameterId != otherPACEInfo.parameterId) {
            return false
        }

        return true
    }

    /**
     * Returns an ASN1 name for the given object identifier.
     * 
     * @param oid a PACE protocol object identifier
     * 
     * @return an ASN1 name if known, or the original object identifier if not
     */
    private fun toProtocolOIDString(oid: String?): String? {
        if (ID_PACE_DH_GM_3DES_CBC_CBC == oid) {
