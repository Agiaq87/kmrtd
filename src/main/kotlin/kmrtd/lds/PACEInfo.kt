        /**
         * Creates a PACE info from an encoding.
         * 
         * @param paceInfoBytes the encoded bytes
         * 
         * @return a PACE info object
         */
        fun createPACEInfo(paceInfoBytes: ByteArray?): PACEInfo {
            /*
     * FIXME: Should add a constructor to PACEInfo that takes byte[] or InputStream, or
     * align this with SecurityInfo.getInstance().
     */
            val sequence = ASN1Sequence.getInstance(paceInfoBytes)
            val oid = (sequence.getObjectAt(0) as ASN1ObjectIdentifier).getId()
            val requiredData = sequence.getObjectAt(1).toASN1Primitive()
            var optionalData: ASN1Primitive? = null
            if (sequence.size() == 3) {
                optionalData = sequence.getObjectAt(2).toASN1Primitive()
            }

            val version = (requiredData as ASN1Integer).value.toInt()
            var parameterId: BigInteger? = null
            if (optionalData != null) {
                parameterId = (optionalData as ASN1Integer).value
            }

            return PACEInfo(oid, version, parameterId)
        }

