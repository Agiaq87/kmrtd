package kmrtd.lds.iso39794

internal interface EncodableEnum<T : Enum<T>> {
    val code: Int

    companion object {
        @JvmStatic
        fun <T> fromCode(code: Int, c: Class<T>): T? {
            for (value in c.getEnumConstants()) {
                if (value is EncodableEnum<*>) {
                    if ((value as EncodableEnum<*>).code == code) {
                        return value
                    }
                }
            }

            return null
        }
    }
}
